# 8. Desenvolva um algoritmo que pergunte ao usuário o estado civil (solteiro, casado, divorciado, viúvo) e exiba uma mensagem correspondente.

estadoCivil = int(input('Digite seu estado civil: 1- Solteiro / 2- Casado / 3- Divorciado / 4- Viuvo \n'))

if estadoCivil == 1:
    print('Estado civil SOLTEIRO')
elif estadoCivil == 2:
    print('Estado civil CASADO')
elif estadoCivil == 1:
    print('Estado civil DIIVORCIADO')
elif estadoCivil == 1:
    print('Estado civil VIUVO')


