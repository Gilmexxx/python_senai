# 6. Desenvolva um programa que pergunte ao usuário uma operação matemática (+, -, *, /) e dois números, e realize a operação escolhida.



numero1 = int(input('Dite o primeiro numero: \n'))
numero2 = int(input('Dite o primeiro numero: \n'))

soma = numero1 + numero2
subtracao = numero1 - numero2
multiplicacao = numero1 * numero2
divisao = numero1 / numero2

print('A soma dos dois numeros é:', {soma})
print('A subtracao dos dois numeros é:', {subtracao})
print('A divisao dos dois numeros é:', {divisao})
print('A multiplicacao dos dois numeros é:', {multiplicacao})
